CREATE TABLE user(
  id BIGINT AUTO_INCREMENT PRIMARY KEY,
  name VARCHAR(16) NOT NULL,
  mobile NUMBER NOT NULL,
  dob TIMESTAMP NOT NULL,
  gender VARCHAR(16) NOT NULL,
  email VARCHAR(16) NOT NULL

);

CREATE TABLE Business(
  id BIGINT AUTO_INCREMENT PRIMARY KEY,
  code VARCHAR(16) NOT NULL,
  name VARCHAR(16) NOT NULL,
  tagLine VARCHAR(16) NOT NULL,
  mobileNumber LONG NOT NULL,
  active BOOLEAN,
  gstNumber LONG NOT NULL,
  tinNumber LONG NOT NULL,
  panNumber LONG NOT NULL,
  bankName VARCHAR(16),
  facebookLink VARCHAR(16),
  twitterLink VARCHAR(16),
  youtubeLink VARCHAR(16),
  instagramLink VARCHAR(16),
  linkedInLink VARCHAR(16)
);

CREATE TABLE RESERVATION(
  RESERVATION_ID BIGINT AUTO_INCREMENT PRIMARY KEY,
  ROOM_ID BIGINT NOT NULL,
  GUEST_ID BIGINT NOT NULL,
  RES_DATE DATE
);

