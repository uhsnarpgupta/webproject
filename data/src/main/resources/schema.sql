CREATE TABLE COUNTRY(
  id BIGINT AUTO_INCREMENT PRIMARY KEY,
  name VARCHAR(30) NOT NULL,
  countrycode VARCHAR(16) NOT NULL,
  continentname VARCHAR(30) NOT NULL,
  created_at DATE,
  valid_to_date DATE
);

